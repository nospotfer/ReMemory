    private void actualitzaPuntuacioTAP() {
        int total = 0;
        int CI = 0;
        int modificador = 0;
        for (JCheckBox cb : llistaCheck){
            if (cb.isSelected()){
                total++;
            }
        }
        
        if (total<5){
            modificador = 0;
        }
        else if(total>4 && total<21){
            modificador = 1;
        }
        else if(total>20){
            modificador = 2;
        }
        
        CI = (total-1)*2+60-modificador;
        
        this.tapTotal.setText(total+"");
        this.tapCi.setText(CI+"");
    }

