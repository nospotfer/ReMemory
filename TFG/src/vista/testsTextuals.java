    private void actualitzaPuntuacioCrc(){
        int total = 0;
        
        // ESCOLARITAT
        if (escoRadio2.isSelected()) total++;
        if (escoRadio3.isSelected()) total += 2;
        if (escoRadio4.isSelected()) total += 3;
        if (escoRadio5.isSelected()) total += 4;
        if (escoRadio6.isSelected()) total += 5;
        
        //ESCOLARITAT PARES
        if (escoParesRadio2.isSelected()) total++;
        if (escoParesRadio3.isSelected()) total += 2;
        
        //CURSOS DE FORMACIÓ
        if (cursosRadio2.isSelected()) total++;
        if (cursosRadio3.isSelected()) total += 2;
        if (cursosRadio4.isSelected()) total += 3;
        
        //OCUPACIÓ LABORAL
        if (ocupacioRadio2.isSelected()) total++;
        if (ocupacioRadio3.isSelected()) total += 2;
        if (ocupacioRadio4.isSelected()) total += 3;
        if (ocupacioRadio5.isSelected()) total += 4;
        
        //FORMACIO MUSICAL
        if (musicalRadio2.isSelected()) total++;
        if (musicalRadio3.isSelected()) total += 2;
        
        //IDIOMES
        if (idiomesRadio2.isSelected()) total++;
        if (idiomesRadio3.isSelected()) total += 2;
        if (idiomesRadio4.isSelected()) total += 3;
        
        //ACTIVITAT LECTORA
        if (lectoraRadio2.isSelected()) total++;
        if (lectoraRadio3.isSelected()) total += 2;
        if (lectoraRadio4.isSelected()) total += 3;
        if (lectoraRadio5.isSelected()) total += 4;
        
        //JOCS INTELECTUALS
        if (jocsRadio2.isSelected()) total++;
        if (jocsRadio3.isSelected()) total += 2;
        
        puntuacioLabel.setText(total+"");
        
        if (total<=6) rangLabel.setText("Rang inferior");
        if (total>6 && total<10) rangLabel.setText("Rang mig-baix");
        if (total>9 && total<15) rangLabel.setText("Rang mig-alt");
        if (total>15) rangLabel.setText("Rang superior");
    }

