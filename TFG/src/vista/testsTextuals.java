    private void actualitzaPuntuacioTAP() {
        int total = 0;
        int CI = 0;
        int modificador = 0;
        for (JCheckBox cb : llistaCheck){
            if (cb.isSelected()){
                total++;
            }
        }
        
        if (total<5){
            modificador = 0;
        }
        else if(total>4 && total<21){
            modificador = 1;
        }
        else if(total>20){
            modificador = 2;
        }
        
        CI = (total-1)*2+60-modificador;
        
        this.tapTotalLabel.setText(total+"");
        this.ciLabel.setText(CI+"");
    }
    private void actualitzaPuntuacioMMSE() {
        int total = 0;
        int parcial = 0;
        int count = 0;
        for (JCheckBox cb : llistaCheckMMSE) {
            if (cb.isSelected()){
                total++;
                parcial++;
            }
            if (count==4){
                calculLabel.setText(parcial+"");
                parcial = 0;
            }
            else if (count == 7){
                diferitLabel.setText(parcial+"");
                parcial = 0;
            }
            else if (count == 12){
                temporalLabel.setText(parcial+"");
                parcial = 0;
            }
            else if (count == 17){
                espacialLabel.setText(parcial+"");
                parcial = 0;
            }
            else if (count == 20){
                recordLabel.setText(parcial+"");
                parcial = 0;
            }
            count++;
        }
        llenguatgeLabel.setText(parcial+"");
        totalMMSELabel.setText(total+"");
    }
